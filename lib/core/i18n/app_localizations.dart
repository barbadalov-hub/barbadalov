import 'package:flutter/widgets.dart';

/// Lightweight, plugin-free localization. Strings live in one table keyed by
/// a dotted id with `en`/`ru`/`uk` variants; missing keys fall back to English
/// then to the key itself. Access from any widget via `context.tr('key')`.
class AppLocalizations {
  final String languageCode;
  const AppLocalizations(this.languageCode);

  static const supportedLocales = [
    Locale('en'),
    Locale('ru'),
    Locale('uk'),
  ];

  static const delegate = _AppLocalizationsDelegate();

  /// The language codes every key must provide.
  static const languages = ['en', 'ru', 'uk'];

  /// The raw string table, exposed for tests (completeness + placeholder checks).
  @visibleForTesting
  static const Map<String, Map<String, String>> values = _values;

  static AppLocalizations of(BuildContext context) =>
      Localizations.of<AppLocalizations>(context, AppLocalizations) ??
      const AppLocalizations('en');

  String tr(String key) {
    final entry = _values[key];
    if (entry == null) return key;
    return entry[languageCode] ?? entry['en'] ?? key;
  }

  /// Like [tr] but replaces `{name}` tokens with [params].
  String trp(String key, Map<String, Object> params) {
    var result = tr(key);
    params.forEach((k, v) => result = result.replaceAll('{$k}', '$v'));
    return result;
  }
}

extension AppLocalizationsX on BuildContext {
  String tr(String key) => AppLocalizations.of(this).tr(key);
  String trp(String key, Map<String, Object> params) =>
      AppLocalizations.of(this).trp(key, params);
}

class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) =>
      ['en', 'ru', 'uk'].contains(locale.languageCode);

  @override
  Future<AppLocalizations> load(Locale locale) async =>
      AppLocalizations(locale.languageCode);

  @override
  bool shouldReload(_AppLocalizationsDelegate old) => false;
}

